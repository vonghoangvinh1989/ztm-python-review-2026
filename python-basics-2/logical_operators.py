# Logical Operators: >, <, ==, >=, <=, !=, and, or, not
print(4 > 5)
print(4 < 5)
print(4 == 5)
print("hello" == "hello")
print("a" > "b")  # False
print("a" > "A")  # True
print(1 < 2 > 3 < 4)  # think like 1 < 2 and 2 > 3 and 3 < 4
print(1 >= 0)
print(1 <= 0)
print(0 != 0)  # != is not equal
print(0 != 1)
print(not (True))  # False
print(not (1 == 1))  # False
