basket = ["a", "x", "b", "c", "d", "e", "d"]
print(len(basket))

# we can reverse a list by using list slicing, create new list
print(basket[::-1])
print(basket)

# range(start, stop)
print(list(range(1, 100)))
print(list(range(100)))
print(list(range(101)))

# join
new_sentence = " ".join(["hi", "my", "name", "is", "JOJO"])
print(new_sentence)
